    {
        return view('admin.footer.links.edit',[
            'footer' => Footer::query()->first()
        ])->with(['activity22' => 'side-menu--active', 'submenu22' => 'side-menu__sub-open']);
    }

    /**
     * Update the specified resource in storage.
